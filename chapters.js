function getChapter(code) {
  const key = String(code);
  if (chapters[key] !== undefined) return chapters[key];
  if (chapters[Number(key)] !== undefined) return chapters[Number(key)];
  for (const k of Object.keys(chapters)) {
    if (chapters[k].code === key) return chapters[k];
  }
  return null;
}

function getIntervention(chapterCode, interventionIdx) {
  const ch = getChapter(chapterCode);
  if (!ch || !ch.interventions) return null;
  return ch.interventions[interventionIdx] || null;
}

function getChaptersInOrder() {
  return CHAPTER_ORDER.map(code => getChapter(code)).filter(Boolean);
}

function getTotalInterventions() {
  return Object.values(chapters).reduce(
    (sum, ch) => sum + (ch.interventions ? ch.interventions.length : 0), 0
  );
}

